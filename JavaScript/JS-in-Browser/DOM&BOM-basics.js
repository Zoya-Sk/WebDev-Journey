/*
  DOM and BOM Basics in JavaScript

  The Document Object Model (DOM) and the Browser Object Model (BOM) are crucial parts of JavaScript for interacting with web pages.

  1. DOM (Document Object Model):
     - Represents the structure of an HTML document as a tree.
     - Allows JavaScript to dynamically modify content, styles, and attributes.
     - Provides methods like getElementById(), querySelector(), createElement(), etc.

  2. BOM (Browser Object Model):
     - Provides access to browser features (outside the document itself).
     - Includes objects like `window`, `navigator`, `location`, `history`, `screen`.
     - Used for tasks like opening new tabs, detecting user agents, navigating history.
*/

